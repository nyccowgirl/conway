package com.company;

/*
Trang Hoang
CS111B - Assignment 7B
 */

import java.util.*;

public class ConwayMain {

    static Scanner input = new Scanner(System.in);

    public static void main(String[] args) {

        ConwayWorld world = new ConwayWorld();

        // Add some live conway cells, in a horizontal line
        for (int i = 0; i < 8; i++) {
            ConwayCell c = new ConwayCell(5, 5 + i, world);
            c.setIsAlive(true);
            world.replaceCell(c);
        }

        // Add some live blinker cells, in a vertical line to a horizontal line
        for (int i = 0; i < 3; i++) {
            BlinkerCell b = new BlinkerCell(14 + i, 7, world);
            b.setIsAlive(true);
            world.replaceCell(b);
        }

        // Add some chaos cells
        ChaosCell ch1 = new ChaosCell(9, 15, world);
        ch1.setIsAlive(true);
        world.replaceCell(ch1);

        ChaosCell ch2 = new ChaosCell(10, 16, world);
        ch2.setIsAlive(true);
        world.replaceCell(ch2);

        ChaosCell ch3 = new ChaosCell(11, 17, world);
        ch3.setIsAlive(true);
        world.replaceCell(ch3);


        // Add an always-alive cells
        AbstractCell a = new AlwaysAliveCell(12, 12, world);
        world.replaceCell(a);

        // Add an always-alive cells
        AbstractCell n = new NeverAliveCell(17, 17, world);
        world.replaceCell(n);

        // Go!
        do {
            world.display();
            world.advanceToNextGeneration();

            System.out.print("Another? (y/n): ");
        } while (input.nextLine().charAt(0) == 'y');
    }
}

